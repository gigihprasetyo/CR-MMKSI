        private bool ValidateDuplicateParamData(List<CRM_xts_workorderCreateParameterDto> lstObjCreate, List<DNetValidationResult> validationResults)
        {
            foreach (var item in lstObjCreate)
            {
                var lst = lstObjCreate.Where(x => x.xts_customernumber.ToString().Trim() == item.xts_customernumber.ToString().Trim() &&
                                                x.xts_reservationid.ToString().Trim() == item.xts_reservationid.ToString().Trim() &&
                                                x.xts_workorder.ToString().Trim() == item.xts_workorder.ToString().Trim());
                if (lst.Count() > 1)
                {
                    String errorMessage = string.Format(string.Format(MessageResource.ErrorMsgDuplicateData) + string.Format(" ({0},{1},{2})", "xts_customernumber = " + item.xts_customernumber.ToString(), "xts_reservationid = " + item.xts_reservationid.ToString(), "xts_workorder = " + item.xts_workorder.ToString()));
                    bool isErrorExist = validationResults.Any(q => q.ErrorMessage == errorMessage);
                    if (isErrorExist == false)
                    {
                        validationResults.Add(new DNetValidationResult(errorMessage));
                    }
                }
            }
            return validationResults.Count == 0;
        }

